// mgmt.register
// -----------------------------------------------------------

// RegisterRequest is the wire shape of POST /v1/repos. Field
// names use lower_snake_case to match the rest of the
// service's JSON style.
//
// Architecture.md §6.2.1 pins the verb signature as
// `mgmt.register(repo_url, default_branch)` — no SHA in the
// request. The handler resolves HEAD via the injected
// [HeadResolver] so the schema-level `current_head_sha NOT
// NULL` invariant holds without exposing the resolution
// machinery on the wire.
type RegisterRequest struct {
	RepoURL       string `json:"repo_url"`
	DefaultBranch string `json:"default_branch"`
}

// RegisterResponse is the wire shape of a successful POST
// /v1/repos. `WebhookSecret` is set only on first
// registration of a given URL — re-registering the same URL
// returns a body WITHOUT `webhook_secret` per the e2e §1
// idempotent-re-register contract and the Stage 7.1 "secret
// revealed exactly once" invariant.
type RegisterResponse struct {
	RepoID         string `json:"repo_id"`
	DefaultBranch  string `json:"default_branch"`
	CurrentHeadSHA string `json:"current_head_sha"`
	// Status is `registered` on a brand-new repo and
	// `exists` when the operator re-registered an existing
	// URL. Lets the operator UI distinguish the two cases
	// without inspecting the absence of `webhook_secret`.
	Status string `json:"status"`
	// WebhookSecret is the per-repo HMAC pre-shared secret
	// returned ONLY on first registration. Omitted from the
	// JSON output when empty (`omitempty`), so a re-register
	// reply never even mentions the field.
	WebhookSecret string `json:"webhook_secret,omitempty"`
	// IngestJobID is the freshly-enqueued full-ingest job's
	// id. Set only on first registration (the operator can
	// poll its state via mgmt.read.repos / mgmt.read.commits
	// once those land in Stage 7.5).
	IngestJobID string `json:"ingest_job_id,omitempty"`
}

func (h *Handler) handleRegister(w http.ResponseWriter, r *http.Request) {
	ctx := r.Context()
	var req RegisterRequest
	if !h.decodeJSONBody(w, r, &req) {
		return
	}

	// --- input validation ---
	repoURL, err := normalizeRepoURL(req.RepoURL)
	if err != nil {
		writeJSONError(w, http.StatusBadRequest, "invalid_request", err.Error())
		return
	}
	branch, err := normalizeRef(req.DefaultBranch)
	if err != nil {
		writeJSONError(w, http.StatusBadRequest, "invalid_request", err.Error())
		return
	}

	// --- resolve HEAD before doing any DB work ---
	//
	// Doing this BEFORE the transaction means a resolver
	// outage cannot leave a half-written `repo` row or an
	// orphan `repo_webhook_secret`. The transaction's
	// ON CONFLICT (url) DO NOTHING handles concurrent
	// register races at the DB level.
	headSHA, err := h.resolver.Resolve(ctx, repoURL, branch)
	if err != nil {
		h.handleResolverError(w, r, "register", repoURL, err)
		return
	}
	if !IsHexGitSHA(headSHA) {
		// Defence-in-depth: a resolver that returns
		// something that isn't a SHA is a contract violation;
		// we treat it as an upstream failure (502) so the
		// operator can retry and the structured log makes
		// the offence visible.
		h.logger.Error("mgmtapi.register.resolver_bad_shape",
			slog.String("op", "register"),
			slog.String("repo_url", repoURL),
			slog.String("returned", headSHA),
		)
		writeJSONError(w, http.StatusBadGateway, "head_resolver_unavailable",
			"head resolver returned a non-SHA value")
		return
	}

	// --- generate the secret BEFORE opening the tx ---
	//
	// A crypto/rand read could in principle fail (it never
	// does on a healthy kernel, but the API permits it).
	// Failing before the tx means we have no partial state
	// to roll back.
	webhookSecret, err := h.secretGen()
	if err != nil {
		h.logger.Error("mgmtapi.register.secret_gen_failed",
			slog.String("op", "register"),
			slog.String("error", err.Error()),
		)
		writeJSONError(w, http.StatusInternalServerError, "internal_error",
			"failed to generate webhook secret")
		return
	}

	// --- atomic write path ---
	resp, err := h.executeRegister(ctx, repoURL, branch, headSHA, webhookSecret)
	if err != nil {
		h.logger.Error("mgmtapi.register.db_failed",
			slog.String("op", "register"),
			slog.String("repo_url", repoURL),
			slog.String("error", err.Error()),
		)
		writeJSONError(w, http.StatusInternalServerError, "internal_error",
			"internal error")
		return
	}

	status := http.StatusOK
	if resp.Status == "registered" {
		status = http.StatusCreated
	}
	subject, _ := SubjectFromContext(ctx)
	h.logger.Info("mgmtapi.register.ok",
		slog.String("op", "register"),
		slog.String("repo_id", resp.RepoID),
		slog.String("status", resp.Status),
		slog.Bool("returned_secret", resp.WebhookSecret != ""),
		slog.String("subject", subject),
		slog.Time("at", h.clock()),
	)
	writeJSONResponse(w, status, resp)
}

// executeRegister runs the register transaction. Separated
// from handleRegister so the SQL shape is testable without
// going through the HTTP layer.
func (h *Handler) executeRegister(ctx context.Context, repoURL, branch, headSHA, webhookSecret string) (RegisterResponse, error) {
	tx, err := h.db.BeginTx(ctx, nil)
	if err != nil {
		return RegisterResponse{}, fmt.Errorf("begin tx: %w", err)
	}
	defer tx.Rollback() //nolint:errcheck // commit path makes rollback a no-op; best-effort cleanup otherwise

	// Step 1: INSERT INTO repo with ON CONFLICT (url) DO
	// NOTHING — the migration 0002 UNIQUE INDEX on
	// repo.url makes this idempotent. RETURNING repo_id::text
	// returns a row only on a NEW insertion.
	const insertRepo = `
		INSERT INTO repo (url, default_branch, current_head_sha)
		VALUES ($1, $2, $3)
		ON CONFLICT (url) DO NOTHING
		RETURNING repo_id::text
	`
	var repoID string
	err = tx.QueryRowContext(ctx, insertRepo, repoURL, branch, headSHA).Scan(&repoID)
	if errors.Is(err, sql.ErrNoRows) {
		// Repo already exists. Fetch its id and short-circuit:
		// the secret stays unrevealed, no new RepoEvent,
		// no new ingest_jobs row (per the e2e §1 idempotent
		// re-register scenario).
		const selectExisting = `
			SELECT repo_id::text, default_branch, current_head_sha
			FROM repo
			WHERE url = $1
		`
		var existingBranch, existingHead string
		if err := tx.QueryRowContext(ctx, selectExisting, repoURL).
			Scan(&repoID, &existingBranch, &existingHead); err != nil {
			return RegisterResponse{}, fmt.Errorf("select existing repo: %w", err)
		}
		if err := tx.Commit(); err != nil {
			return RegisterResponse{}, fmt.Errorf("commit tx (existing): %w", err)
		}
		return RegisterResponse{
			RepoID:         repoID,
			DefaultBranch:  existingBranch,
			CurrentHeadSHA: existingHead,
			Status:         "exists",
		}, nil
	}
	if err != nil {
		return RegisterResponse{}, fmt.Errorf("insert repo: %w", err)
	}

	// Step 2: stash the webhook secret. Migration 0018 makes
	// `repo_id` the PK, so this also rejects a duplicate
	// secret-write if anybody ever bypasses the
	// `ON CONFLICT` path above.
	const insertSecret = `
		INSERT INTO repo_webhook_secret (repo_id, webhook_secret)
		VALUES ($1::uuid, $2)
	`
	if _, err := tx.ExecContext(ctx, insertSecret, repoID, webhookSecret); err != nil {
		return RegisterResponse{}, fmt.Errorf("insert repo_webhook_secret: %w", err)
	}

	// Step 3: audit event (kind='register'). architecture.md
	// §5.6 line 644 pins the closed set of kinds; ENUM
	// migration 0001 enforces it at the DB.
	const insertEvent = `
		INSERT INTO repo_event (repo_id, kind, from_sha, to_sha)
		VALUES ($1::uuid, 'register'::repo_event_kind, NULL, $2)
	`
	if _, err := tx.ExecContext(ctx, insertEvent, repoID, headSHA); err != nil {
		return RegisterResponse{}, fmt.Errorf("insert repo_event: %w", err)
	}

	// Step 4: enqueue the full-ingest job. ON CONFLICT DO
	// NOTHING handles the (vanishingly unlikely) race where
	// a parallel register lost the repo INSERT race but won
	// the job INSERT race — exit cleanly either way.
	const insertJob = `
		INSERT INTO ingest_jobs (repo_id, mode, from_sha, to_sha)
		VALUES ($1::uuid, 'full'::ingest_mode, NULL, $2)
		ON CONFLICT (repo_id, mode, (COALESCE(from_sha, '')), to_sha) DO NOTHING
		RETURNING job_id::text
	`
	var jobID string
	err = tx.QueryRowContext(ctx, insertJob, repoID, headSHA).Scan(&jobID)
	if err != nil && !errors.Is(err, sql.ErrNoRows) {
		return RegisterResponse{}, fmt.Errorf("insert ingest_jobs: %w", err)
	}

	if err := tx.Commit(); err != nil {
		return RegisterResponse{}, fmt.Errorf("commit tx: %w", err)
	}
	return RegisterResponse{
		RepoID:         repoID,
		DefaultBranch:  branch,
		CurrentHeadSHA: headSHA,
		Status:         "registered",
		WebhookSecret:  webhookSecret,
		IngestJobID:    jobID,
	}, nil
}

// -----------------------------------------------------------
// mgmt.ingest
// -----------------------------------------------------------

// IngestRequest is the wire shape of POST /v1/repos/{id}/ingest.
// `SHA` is optional; if empty, the handler resolves HEAD via
// the [HeadResolver] (the architecture pins the verb signature
// as `mgmt.ingest(repo_id, sha?)`).
type IngestRequest struct {
	SHA string `json:"sha,omitempty"`
}

// IngestResponse is the wire shape of a successful POST
// /v1/repos/{id}/ingest. The endpoint is asynchronous —
// `status` reflects the freshly-enqueued / pre-existing job
// state, and the caller polls `mgmt.read.repos` for ingest
// progress (Stage 7.5).
type IngestResponse struct {
	IngestJobID string `json:"ingest_job_id"`
	JobState    string `json:"job_state"`
	Mode        string `json:"mode"`
	ToSHA       string `json:"to_sha"`
	// Deduped is true when the job already existed; useful
	// to operators triaging "did my retry land or did I
	// double-trigger?" alerts.
	Deduped bool `json:"deduped"`
}

func (h *Handler) handleIngest(w http.ResponseWriter, r *http.Request, rawRepoID string) {
	ctx := r.Context()

	if !reUUID.MatchString(rawRepoID) {
		writeJSONError(w, http.StatusBadRequest, "invalid_repo_id",
			"repo_id path segment is not a valid UUID")
		return
	}

	var req IngestRequest
	if !h.decodeJSONBody(w, r, &req) {
		return
	}

	// Validate the supplied SHA shape BEFORE any DB read. An
	// operator-supplied bad SHA should always be 400, never
	// masked by a transient DB outage as a 500. The default-
	// HEAD case (empty SHA) requires loadRepo + resolver
	// downstream, but a non-empty operator SHA short-circuits
	// here.
	sha := strings.TrimSpace(req.SHA)
	if sha != "" && !IsHexGitSHA(sha) {
		writeJSONError(w, http.StatusBadRequest, "invalid_sha",
			"sha must be a 40- or 64-char lower-case hex git SHA")
		return
	}

	// Look up the repo so we can default sha against a live
	// HEAD resolve (when no SHA was supplied) AND return 404
	// cleanly when the id is well-formed but unknown.
	repoURL, branch, _, err := h.loadRepo(ctx, rawRepoID)
	if errors.Is(err, sql.ErrNoRows) {
		writeJSONError(w, http.StatusNotFound, "repo_not_found",
			"no repo with the supplied repo_id")
		return
	}
	if err != nil {
		h.logger.Error("mgmtapi.ingest.load_repo_failed",
			slog.String("op", "ingest"),
			slog.String("repo_id", rawRepoID),
			slog.String("error", err.Error()),
		)
		writeJSONError(w, http.StatusInternalServerError, "internal_error", "internal error")
		return
	}

	if sha == "" {
		// "Default HEAD" semantics: ask the live resolver.
		// We do NOT silently fall back to the cached
		// `repo.current_head_sha` — Stage 7.1 write verbs
		// have no degraded-mode contract, and quietly
		// enqueueing the cached SHA would let a stale value
		// hide a resolver outage from the operator. Any
		// resolver failure is surfaced explicitly: 400 on
		// "ref not found", 502 on transient outage.
		resolved, rerr := h.resolver.Resolve(ctx, repoURL, branch)
		if rerr != nil {
			h.handleResolverError(w, r, "ingest", repoURL, rerr)
			return
		}
		if !IsHexGitSHA(resolved) {
			// A resolver that returned a non-hex value is
			// itself a bug / outage. Treat as 502 so the
			// operator can retry / page the team owning
			// the resolver.
			h.logger.Error("mgmtapi.ingest.resolver_returned_non_sha",
				slog.String("op", "ingest"),
				slog.String("repo_id", rawRepoID),
				slog.String("resolved", resolved),
			)
			writeJSONError(w, http.StatusBadGateway, "head_resolver_unavailable",
				"head resolver returned a non-SHA value")
			return
		}
		sha = resolved
	}

	resp, err := h.executeIngest(ctx, rawRepoID, sha)
	if err != nil {
		if errors.Is(err, sql.ErrNoRows) || isForeignKeyViolation(err) {
			// Repo was deleted between loadRepo and the
			// INSERT (unlikely but possible). Surface 404
			// instead of 500.
			writeJSONError(w, http.StatusNotFound, "repo_not_found",
				"no repo with the supplied repo_id")
			return
		}
		h.logger.Error("mgmtapi.ingest.db_failed",
			slog.String("op", "ingest"),
			slog.String("repo_id", rawRepoID),
			slog.String("to_sha", sha),
			slog.String("error", err.Error()),
		)
		writeJSONError(w, http.StatusInternalServerError, "internal_error", "internal error")
		return
	}

	subject, _ := SubjectFromContext(ctx)
	h.logger.Info("mgmtapi.ingest.ok",
		slog.String("op", "ingest"),
		slog.String("repo_id", rawRepoID),
		slog.String("ingest_job_id", resp.IngestJobID),
		slog.String("job_state", resp.JobState),
		slog.String("to_sha", sha),
		slog.Bool("deduped", resp.Deduped),
		slog.String("subject", subject),
		slog.Time("at", h.clock()),
	)
	writeJSONResponse(w, http.StatusAccepted, resp)
}

func (h *Handler) executeIngest(ctx context.Context, repoID, toSHA string) (IngestResponse, error) {
	tx, err := h.db.BeginTx(ctx, nil)
	if err != nil {
		return IngestResponse{}, fmt.Errorf("begin tx: %w", err)
	}
	defer tx.Rollback() //nolint:errcheck

	// Try to enqueue. ON CONFLICT DO NOTHING returns no row
	// when the same (repo_id, mode='full', '', to_sha)
	// tuple already exists; we then SELECT the existing
	// row's id and skip the RepoEvent write (so a retry
	// does not pollute the audit log).
	const insertJob = `
		INSERT INTO ingest_jobs (repo_id, mode, from_sha, to_sha)
		VALUES ($1::uuid, 'full'::ingest_mode, NULL, $2)
		ON CONFLICT (repo_id, mode, (COALESCE(from_sha, '')), to_sha) DO NOTHING
		RETURNING job_id::text, status::text
	`
	var jobID, jobState string
	err = tx.QueryRowContext(ctx, insertJob, repoID, toSHA).Scan(&jobID, &jobState)
	switch {
	case err == nil:
		// New row; write the matching RepoEvent.
		const insertEvent = `
			INSERT INTO repo_event (repo_id, kind, from_sha, to_sha)
			VALUES ($1::uuid, 'manual'::repo_event_kind, NULL, $2)
		`
		if _, err := tx.ExecContext(ctx, insertEvent, repoID, toSHA); err != nil {
			return IngestResponse{}, fmt.Errorf("insert repo_event: %w", err)
		}
		if err := tx.Commit(); err != nil {
			return IngestResponse{}, fmt.Errorf("commit tx: %w", err)
		}
		return IngestResponse{
			IngestJobID: jobID, JobState: jobState, Mode: "full", ToSHA: toSHA, Deduped: false,
		}, nil
	case errors.Is(err, sql.ErrNoRows):
		// Existing job. Look it up and DO NOT write a new
		// RepoEvent — the audit log already has the
		// first-call's event row.
		const selectExisting = `
			SELECT job_id::text, status::text
			FROM ingest_jobs
			WHERE repo_id = $1::uuid
			  AND mode = 'full'::ingest_mode
			  AND COALESCE(from_sha, '') = ''
			  AND to_sha = $2
		`
		if err := tx.QueryRowContext(ctx, selectExisting, repoID, toSHA).
			Scan(&jobID, &jobState); err != nil {
			return IngestResponse{}, fmt.Errorf("select existing ingest_jobs: %w", err)
		}
		if err := tx.Commit(); err != nil {
			return IngestResponse{}, fmt.Errorf("commit tx (existing): %w", err)
		}
		return IngestResponse{
			IngestJobID: jobID, JobState: jobState, Mode: "full", ToSHA: toSHA, Deduped: true,
		}, nil
	default:
		return IngestResponse{}, fmt.Errorf("insert ingest_jobs: %w", err)
	}
}

