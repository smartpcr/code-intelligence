// IngestDeltaRequest is the wire shape of POST
// /v1/repos/{id}/ingest_delta. Both SHAs are REQUIRED — the
// architecture verb signature is `mgmt.ingest_delta(repo_id,
// from_sha, to_sha)`. A delta with no from-SHA is by
// definition a full ingest and should go through mgmt.ingest.
type IngestDeltaRequest struct {
	FromSHA string `json:"from_sha"`
	ToSHA   string `json:"to_sha"`
}

// IngestDeltaResponse is the wire shape of a successful POST
// /v1/repos/{id}/ingest_delta. The `Deduped` flag is the
// public signal of the idempotency contract — operators
// retrying a delta-ingest call see `deduped=true` and the
// same `ingest_job_id` they got the first time.
type IngestDeltaResponse struct {
	IngestJobID string `json:"ingest_job_id"`
	JobState    string `json:"job_state"`
	Mode        string `json:"mode"`
	FromSHA     string `json:"from_sha"`
	ToSHA       string `json:"to_sha"`
	Deduped     bool   `json:"deduped"`
}

func (h *Handler) handleIngestDelta(w http.ResponseWriter, r *http.Request, rawRepoID string) {
	ctx := r.Context()

	if !reUUID.MatchString(rawRepoID) {
		writeJSONError(w, http.StatusBadRequest, "invalid_repo_id",
			"repo_id path segment is not a valid UUID")
		return
	}

	var req IngestDeltaRequest
	if !h.decodeJSONBody(w, r, &req) {
		return
	}

	from := strings.TrimSpace(req.FromSHA)
	to := strings.TrimSpace(req.ToSHA)
	if from == "" {
		writeJSONError(w, http.StatusBadRequest, "invalid_request",
			"from_sha: required")
		return
	}
	if to == "" {
		writeJSONError(w, http.StatusBadRequest, "invalid_request",
			"to_sha: required")
		return
	}
	if !IsHexGitSHA(from) {
		writeJSONError(w, http.StatusBadRequest, "invalid_sha",
			"from_sha must be a 40- or 64-char lower-case hex git SHA")
		return
	}
	if !IsHexGitSHA(to) {
		writeJSONError(w, http.StatusBadRequest, "invalid_sha",
			"to_sha must be a 40- or 64-char lower-case hex git SHA")
		return
	}
	if from == to {
		writeJSONError(w, http.StatusBadRequest, "invalid_request",
			"from_sha and to_sha must differ")
		return
	}

	resp, err := h.executeIngestDelta(ctx, rawRepoID, from, to)
	if err != nil {
		if errors.Is(err, sql.ErrNoRows) || isForeignKeyViolation(err) {
			writeJSONError(w, http.StatusNotFound, "repo_not_found",
				"no repo with the supplied repo_id")
			return
		}
		h.logger.Error("mgmtapi.ingest_delta.db_failed",
			slog.String("op", "ingest_delta"),
			slog.String("repo_id", rawRepoID),
			slog.String("from_sha", from),
			slog.String("to_sha", to),
			slog.String("error", err.Error()),
		)
		writeJSONError(w, http.StatusInternalServerError, "internal_error", "internal error")
		return
	}

	subject, _ := SubjectFromContext(ctx)
	h.logger.Info("mgmtapi.ingest_delta.ok",
		slog.String("op", "ingest_delta"),
		slog.String("repo_id", rawRepoID),
		slog.String("ingest_job_id", resp.IngestJobID),
		slog.String("job_state", resp.JobState),
		slog.String("from_sha", from),
		slog.String("to_sha", to),
		slog.Bool("deduped", resp.Deduped),
		slog.String("subject", subject),
		slog.Time("at", h.clock()),
	)
	writeJSONResponse(w, http.StatusAccepted, resp)
}

func (h *Handler) executeIngestDelta(ctx context.Context, repoID, fromSHA, toSHA string) (IngestDeltaResponse, error) {
	tx, err := h.db.BeginTx(ctx, nil)
	if err != nil {
		return IngestDeltaResponse{}, fmt.Errorf("begin tx: %w", err)
	}
	defer tx.Rollback() //nolint:errcheck

	// ON CONFLICT (repo_id, mode, (COALESCE(from_sha, '')),
	// to_sha) DO NOTHING is the canonical idempotent
	// delta-enqueue. RETURNING is empty on conflict; that's
	// the signal we use to skip the RepoEvent insert and
	// look up the existing job.
	const insertJob = `
		INSERT INTO ingest_jobs (repo_id, mode, from_sha, to_sha)
		VALUES ($1::uuid, 'delta'::ingest_mode, $2, $3)
		ON CONFLICT (repo_id, mode, (COALESCE(from_sha, '')), to_sha) DO NOTHING
		RETURNING job_id::text, status::text
	`
	var jobID, jobState string
	err = tx.QueryRowContext(ctx, insertJob, repoID, fromSHA, toSHA).Scan(&jobID, &jobState)
	switch {
	case err == nil:
		const insertEvent = `
			INSERT INTO repo_event (repo_id, kind, from_sha, to_sha)
			VALUES ($1::uuid, 'manual'::repo_event_kind, $2, $3)
		`
		if _, err := tx.ExecContext(ctx, insertEvent, repoID, fromSHA, toSHA); err != nil {
			return IngestDeltaResponse{}, fmt.Errorf("insert repo_event: %w", err)
		}
		if err := tx.Commit(); err != nil {
			return IngestDeltaResponse{}, fmt.Errorf("commit tx: %w", err)
		}
		return IngestDeltaResponse{
			IngestJobID: jobID, JobState: jobState, Mode: "delta",
			FromSHA: fromSHA, ToSHA: toSHA, Deduped: false,
		}, nil
	case errors.Is(err, sql.ErrNoRows):
		const selectExisting = `
			SELECT job_id::text, status::text
			FROM ingest_jobs
			WHERE repo_id = $1::uuid
			  AND mode = 'delta'::ingest_mode
			  AND COALESCE(from_sha, '') = $2
			  AND to_sha = $3
		`
		if err := tx.QueryRowContext(ctx, selectExisting, repoID, fromSHA, toSHA).
			Scan(&jobID, &jobState); err != nil {
			return IngestDeltaResponse{}, fmt.Errorf("select existing ingest_jobs: %w", err)
		}
		if err := tx.Commit(); err != nil {
			return IngestDeltaResponse{}, fmt.Errorf("commit tx (existing): %w", err)
		}
		return IngestDeltaResponse{
			IngestJobID: jobID, JobState: jobState, Mode: "delta",
			FromSHA: fromSHA, ToSHA: toSHA, Deduped: true,
		}, nil
	default:
		return IngestDeltaResponse{}, fmt.Errorf("insert ingest_jobs: %w", err)
	}
}

