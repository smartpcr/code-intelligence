// Route paths for the Stage 7.1 verbs. The trailing-resource
// segments (`/{id}/ingest`, `/{id}/ingest_delta`) are parsed
// by extractIngestPath; we DO NOT use net/http 1.22 method-
// based routing because the same handler also serves the
// 405 / 404 envelopes uniformly across every verb.
const (
	// RouteRepos is the prefix every Stage 7.1 route falls under.
	// Operators register a repo via POST /v1/repos and trigger
	// per-repo ingests via POST /v1/repos/{repo_id}/ingest[_delta].
	RouteRepos = "/v1/repos"

	// ingestSuffix is the trailing path segment that
	// distinguishes a full-ingest request from the register
	// surface (which has no suffix).
	ingestSuffix = "/ingest"

	// ingestDeltaSuffix is the trailing path segment for
	// `mgmt.ingest_delta`. Must be matched BEFORE
	// `ingestSuffix` since `/ingest` is a strict prefix of
	// `/ingest_delta` only after a literal `_`.
	ingestDeltaSuffix = "/ingest_delta"
)

// DefaultMaxBodyBytes caps the inbound JSON body so a
// malformed-but-authenticated request cannot exhaust process
// memory. 64 KiB is comfortably larger than any operator-shape
// request (the largest is mgmt.register with a URL, default
// branch, and a few flags — < 1 KiB realistically) and small
// enough that an O(1000) concurrent flood costs ~64 MiB at
// worst.
const DefaultMaxBodyBytes int64 = 64 << 10

// HMACSecretBytes is the entropy (in raw bytes) of the
// per-repo webhook HMAC secret generated by mgmt.register. The
// secret is hex-encoded for storage / transport, so the wire
// width is 2x this value. 32 bytes = 256 bits matches the
// HMAC-SHA256 key length recommended by RFC 2104; longer
// secrets do not add cryptographic strength for SHA-256 but
// cost more bytes everywhere.
const HMACSecretBytes = 32

// reUUID matches the RFC 4122 hex-and-hyphen shape. The DB
// `$1::uuid` cast is the canonical source of truth, but we
// pre-validate at the handler layer so a malformed path
// component returns 400 (operator error) rather than 500
// (the DB error would otherwise leak as an opaque
// `pq.Error`).
//
// Case-insensitive on hex digits per RFC 4122 §3.
var reUUID = regexp.MustCompile(
	`^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}$`)

// Options bundles the optional knobs callers pass to
// [NewHandler]. Every field has a sensible default if zero-
// valued so the cmd binary can construct a working handler
// with just (db, verifier, resolver).
type Options struct {
	// Logger receives one structured record per request.
	// Defaults to slog.Default(). The handler NEVER logs the
	// webhook secret, the bearer token, the
	// `Authorization` header value, or the raw request
	// body.
	Logger *slog.Logger

	// MaxBodyBytes caps the request body the handler will
	// read. Zero means [DefaultMaxBodyBytes]; negative
	// disables the cap (NOT recommended in production).
	MaxBodyBytes int64

	// SecretGen overrides the function used to generate
	// per-repo webhook HMAC secrets. Defaults to a
	// crypto/rand-backed implementation that produces
	// [HMACSecretBytes] bytes of entropy hex-encoded.
	// Tests override to make the generated value
	// deterministic; production passes nil.
	SecretGen func() (string, error)

	// Clock is the time source the handler uses for
	// structured-log `received_at` fields. Database
	// timestamps use the server-side `now()` so a fixed
	// Go-side clock does NOT shift DB columns; tests that
	// need a frozen log timestamp inject one.
	Clock func() time.Time
}

// Handler is the Management API HTTP handler. Construct one
// per process via [NewHandler]; the same handler is safe for
// concurrent use across goroutines.
type Handler struct {
	db        *sql.DB
	verifier  TokenVerifier
	resolver  HeadResolver
	logger    *slog.Logger
	maxBody   int64
	secretGen func() (string, error)
	clock     func() time.Time

	// mux is the http.Handler the public ServeHTTP delegates
	// to after auth. Constructed in NewHandler so route
	// registration runs once.
	mux http.Handler
}

// NewHandler wires the Stage 7.1 routes onto `db`, `verifier`,
// and `resolver`. Panics on a nil *sql.DB, nil verifier, or
// nil resolver — none of those have a meaningful zero value
// in this context, and a process that calls NewHandler with
// any of them missing is a configuration error the operator
// should see at boot, not at the first request.
func NewHandler(db *sql.DB, verifier TokenVerifier, resolver HeadResolver, opts Options) *Handler {
	if db == nil {
		panic("mgmtapi: NewHandler: nil *sql.DB")
	}
	if verifier == nil {
		panic("mgmtapi: NewHandler: nil TokenVerifier")
	}
	if resolver == nil {
		panic("mgmtapi: NewHandler: nil HeadResolver")
	}
	logger := opts.Logger
	if logger == nil {
		logger = slog.Default()
	}
	maxBody := opts.MaxBodyBytes
	if maxBody == 0 {
		maxBody = DefaultMaxBodyBytes
	}
	clock := opts.Clock
	if clock == nil {
		clock = time.Now
	}
	secretGen := opts.SecretGen
	if secretGen == nil {
		secretGen = defaultSecretGen
	}
	h := &Handler{
		db:        db,
		verifier:  verifier,
		resolver:  resolver,
		logger:    logger,
		maxBody:   maxBody,
		secretGen: secretGen,
		clock:     clock,
	}
	// Build the inner mux. The outer ServeHTTP runs auth
	// FIRST so we wrap this once. Method gating happens
	// inside each verb so we can emit a uniform JSON 405
	// envelope on the wrong verb.
	inner := http.HandlerFunc(h.route)
	h.mux = authMiddleware(verifier, logger, inner)
	return h
}

// ServeHTTP is the public entry point. Delegates to the auth-
// gated mux; the auth layer 401-rejects before any request
// body is read.
func (h *Handler) ServeHTTP(w http.ResponseWriter, r *http.Request) {
	h.mux.ServeHTTP(w, r)
}

// route dispatches an authenticated request to the right verb
// handler. Method gating + path parsing live here so every
// verb shares the same 404 / 405 envelopes.
func (h *Handler) route(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		w.Header().Set("Allow", http.MethodPost)
		writeJSONError(w, http.StatusMethodNotAllowed, "method_not_allowed",
			fmt.Sprintf("method %s not allowed; use POST", r.Method))
		return
	}

	// /v1/repos               -> register
	// /v1/repos/{id}/ingest   -> ingest
	// /v1/repos/{id}/ingest_delta -> ingest_delta
	switch {
	case r.URL.Path == RouteRepos, r.URL.Path == RouteRepos+"/":
		h.handleRegister(w, r)
		return
	case strings.HasPrefix(r.URL.Path, RouteRepos+"/"):
		repoID, suffix, ok := extractIngestPath(r.URL.Path)
		if !ok {
			writeJSONError(w, http.StatusNotFound, "not_found",
				"unknown management API route")
			return
		}
		switch suffix {
		case ingestSuffix:
			h.handleIngest(w, r, repoID)
		case ingestDeltaSuffix:
			h.handleIngestDelta(w, r, repoID)
		default:
			writeJSONError(w, http.StatusNotFound, "not_found",
				"unknown management API route")
		}
		return
	default:
		writeJSONError(w, http.StatusNotFound, "not_found",
			"unknown management API route")
	}
}

// extractIngestPath parses `/v1/repos/{repo_id}/ingest` or
// `/v1/repos/{repo_id}/ingest_delta`. Returns the raw repo_id
// string and the suffix (`/ingest` or `/ingest_delta`). The
// repo_id is NOT validated as a UUID here — that's the verb
// handler's job so 400 / 404 attribution stays clean.
//
// Returns ok=false for any path that does not match either
// shape exactly.
func extractIngestPath(path string) (repoID, suffix string, ok bool) {
	rest := strings.TrimPrefix(path, RouteRepos+"/")
	// Match the longest suffix first: `/ingest_delta` is a
	// strict superset of `/ingest`.
	for _, s := range []string{ingestDeltaSuffix, ingestSuffix} {
		if strings.HasSuffix(rest, s) {
			id := strings.TrimSuffix(rest, s)
			// Reject extra path segments like
			// `/v1/repos/{id}/foo/ingest`. The repo_id
			// segment MUST be the only thing before
			// the suffix.
			if id == "" || strings.Contains(id, "/") {
				return "", "", false
			}
			return id, s, true
		}
	}
	return "", "", false
}

