// ErrorEnvelope is the shape every non-2xx response carries.
// Keeping a single shape across all verbs lets the operator
// UI render errors uniformly.
//
// `Code` is a stable lower_snake_case enum the UI / clients
// can dispatch on (e.g. `invalid_request`, `repo_not_found`,
// `head_resolver_unavailable`). `Message` is operator-facing
// human-readable text — short, never leaks driver / SQL
// detail, safe to render.
type ErrorEnvelope struct {
	Error   bool   `json:"error"`
	Code    string `json:"code"`
	Message string `json:"message"`
	// Degraded mirrors the §6.3 degraded-mode contract
	// shape for read paths; included on every response so
	// downstream clients can rely on the field always being
	// present even when the write path itself is fully
	// healthy. Stage 7.1 always returns `false` here —
	// write verbs do not have a degraded mode in the
	// architecture contract.
	Degraded bool `json:"degraded"`
}

// DegradedEnvelope wraps a successful response with the
// `degraded` / `degraded_reason` fields that architecture.md
// §6.3 promises for every mgmt.* verb. Stage 7.1 write
// verbs never serve a degraded response, but the helper is
// here so the downstream read verbs (Stage 7.5) can share
// the same envelope shape without a follow-up refactor.
//
// Encoding rule: callers `json.Marshal` the payload struct,
// then this helper merges the degraded fields with the
// payload at the top level. Implemented via a generic
// wrapper struct rather than `map[string]any` so a payload
// type with non-string fields round-trips cleanly.
type DegradedEnvelope[T any] struct {
	// Embedded so the JSON-marshaled top-level field set is
	// the payload's fields PLUS the degraded fields.
	Payload T
	// Degraded is true iff the verb fell back to a
	// degraded surface (cached snapshot, stale read, etc).
	// For Stage 7.1 write verbs this stays false.
	Degraded bool `json:"degraded"`
	// DegradedReason is a short operator-facing string
	// explaining the fallback. Omitted on non-degraded
	// responses (`omitempty`).
	DegradedReason string `json:"degraded_reason,omitempty"`
}

// MarshalJSON implements [json.Marshaler] for
// [DegradedEnvelope]. It produces a flat JSON object with the
// payload's fields merged at the top level alongside
// `degraded` / `degraded_reason`.
//
// The helper is used by Stage 7.5 read verbs once they land;
// Stage 7.1 write verbs do not invoke it in the happy path
// (the responses they produce are pure success shapes). The
// type and method exist now so the §6.3 envelope contract is
// satisfied without a future, surprise-prone refactor.
func (d DegradedEnvelope[T]) MarshalJSON() ([]byte, error) {
	// Marshal the payload first; if the payload type is
	// itself a struct, this produces a JSON object we can
	// peel the outer braces off of and re-emit with the
	// extra fields appended.
	pb, err := json.Marshal(d.Payload)
	if err != nil {
		return nil, fmt.Errorf("DegradedEnvelope: payload marshal: %w", err)
	}
	if len(pb) < 2 || pb[0] != '{' || pb[len(pb)-1] != '}' {
		// Non-object payload (number, string, array, null,
		// boolean). The §6.3 envelope is only well-defined
		// for object responses; the v1 surface uses object
		// payloads exclusively.
		return nil, fmt.Errorf("DegradedEnvelope: payload is not a JSON object: %s", pb)
	}
	extra := map[string]any{"degraded": d.Degraded}
	if d.DegradedReason != "" {
		extra["degraded_reason"] = d.DegradedReason
	}
	xb, err := json.Marshal(extra)
	if err != nil {
		return nil, fmt.Errorf("DegradedEnvelope: extra marshal: %w", err)
	}
	if len(xb) == 2 { // "{}"
		return pb, nil
	}
	// Merge: drop the trailing `}` from `pb`, drop the
	// leading `{` from `xb`, glue with a comma.
	out := make([]byte, 0, len(pb)+len(xb)+1)
	out = append(out, pb[:len(pb)-1]...)
	if len(pb) > 2 { // payload has at least one field already
		out = append(out, ',')
	}
	out = append(out, xb[1:]...)
	return out, nil
}

// writeJSONResponse writes the JSON encoding of `payload`
// with the supplied status code and the `application/json`
// content-type header. Errors during encoding land in the
// logger but don't change the response status — the headers
// are already on the wire by then.
func writeJSONResponse(w http.ResponseWriter, status int, payload any) {
	w.Header().Set("Content-Type", "application/json; charset=utf-8")
	w.WriteHeader(status)
	_ = json.NewEncoder(w).Encode(payload)
}

// writeJSONError writes an [ErrorEnvelope] response. Errors
// during encoding are silently dropped (the body has already
// started — there's nothing useful we can do).
func writeJSONError(w http.ResponseWriter, status int, code, message string) {
	w.Header().Set("Content-Type", "application/json; charset=utf-8")
	w.WriteHeader(status)
	_ = json.NewEncoder(w).Encode(ErrorEnvelope{
		Error: true, Code: code, Message: message,
	})
}
