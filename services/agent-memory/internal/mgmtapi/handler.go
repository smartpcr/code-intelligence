// loadRepo fetches the columns the ingest verbs need to
// resolve a default SHA against the cached HEAD. Returns
// [sql.ErrNoRows] when the id is well-formed but not found.
//
// Also classifies a PostgreSQL `invalid input syntax for
// type uuid` error as `sql.ErrNoRows` so a future code path
// that hits this helper with an unvalidated id still falls
// onto the 404 branch instead of leaking a 500.
func (h *Handler) loadRepo(ctx context.Context, repoID string) (repoURL, defaultBranch, currentHead string, err error) {
	const q = `
		SELECT url, default_branch, current_head_sha
		FROM repo
		WHERE repo_id = $1::uuid
	`
	err = h.db.QueryRowContext(ctx, q, repoID).Scan(&repoURL, &defaultBranch, &currentHead)
	if err != nil && isInvalidUUIDError(err) {
		return "", "", "", sql.ErrNoRows
	}
	return repoURL, defaultBranch, currentHead, err
}

// decodeJSONBody decodes the request body into `dst` and
// returns false on any error after writing the appropriate
// 4xx response. Imposes the configured body cap so a
// pathological body cannot exhaust memory.
//
// JSON shape rules:
//
//   - The body must be a single JSON object — top-level
//     arrays, numbers, or nulls are rejected by `json.Decode`
//     into a struct.
//   - Unknown fields are ALLOWED (forward-compat with
//     newer clients). Tightening this is a future change.
//   - Empty body is accepted iff `dst` has no required
//     fields — required-field validation is the verb
//     handler's job.
func (h *Handler) decodeJSONBody(w http.ResponseWriter, r *http.Request, dst any) bool {
	if h.maxBody > 0 {
		r.Body = http.MaxBytesReader(w, r.Body, h.maxBody)
	}
	body, err := io.ReadAll(r.Body)
	if err != nil {
		var mxErr *http.MaxBytesError
		if errors.As(err, &mxErr) {
			writeJSONError(w, http.StatusRequestEntityTooLarge,
				"body_too_large",
				fmt.Sprintf("request body exceeds %d bytes", h.maxBody))
			return false
		}
		writeJSONError(w, http.StatusBadRequest, "bad_request",
			"failed to read request body")
		return false
	}
	if len(body) == 0 {
		// An empty body decodes to the zero value of `dst`;
		// callers run required-field validation explicitly.
		return true
	}
	if err := json.Unmarshal(body, dst); err != nil {
		writeJSONError(w, http.StatusBadRequest, "invalid_json",
			"request body is not valid JSON")
		return false
	}
	return true
}

// normalizeRepoURL validates and canonicalises an operator-
// supplied repo URL. The schema treats `repo.url` as opaque
// text, but the UNIQUE INDEX on it means inconsistent
// trailing slashes / scheme casing would defeat
// idempotency. We:
//
//   - Trim leading / trailing whitespace.
//   - Require a non-empty value.
//   - Require a parseable URL with a scheme (http / https /
//     ssh / git+ssh) and a host. A bare path like `acme/svc`
//     is rejected so the audit log never carries an
//     unresolvable identifier.
//
// The handler does NOT touch URL case (some git hosts treat
// the path as case-sensitive) and does NOT strip trailing
// slashes or `.git` suffixes — those are part of the
// canonical URL the operator chose.
func normalizeRepoURL(raw string) (string, error) {
	raw = strings.TrimSpace(raw)
	if raw == "" {
		return "", errors.New("repo_url: required")
	}
	u, err := url.Parse(raw)
	if err != nil {
		return "", fmt.Errorf("repo_url: %v", err)
	}
	if u.Scheme == "" {
		return "", errors.New("repo_url: missing scheme (use https://, ssh://, or git@host:path)")
	}
	// SCP-style URLs (`git@host:path`) get parsed as a
	// path-only URL with Scheme=="" — we already reject
	// those above. A real git host URL has either a Host
	// (https://) or an Opaque path (ssh://user@host/path).
	if u.Host == "" && u.Opaque == "" {
		return "", errors.New("repo_url: missing host")
	}
	return raw, nil
}

// defaultSecretGen produces a fresh, hex-encoded
// [HMACSecretBytes]-byte random secret. Used by the
// production composition root and as the default when
// Options.SecretGen is nil.
//
// crypto/rand.Read returns an error only under exceptional
// host conditions (the OS RNG device is unreachable); the
// handler treats that as a 500 internal error so the
// operator can retry. Note: we deliberately do NOT silently
// fall back to math/rand — a non-cryptographic secret would
// be worse than no secret at all because callers downstream
// trust the value to be HMAC-keyworthy.
func defaultSecretGen() (string, error) {
	buf := make([]byte, HMACSecretBytes)
	if _, err := rand.Read(buf); err != nil {
		return "", fmt.Errorf("crypto/rand: %w", err)
	}
	return hex.EncodeToString(buf), nil
}

// handleResolverError maps a HeadResolver-returned error onto
// the matching response. Centralised so the register and
// ingest verbs apply identical classification rules.
func (h *Handler) handleResolverError(w http.ResponseWriter, r *http.Request, op, repoURL string, err error) {
	switch {
	case errors.Is(err, ErrHeadResolverUnknownRef):
		writeJSONError(w, http.StatusBadRequest, "unknown_ref",
			"default branch was not found on the remote")
	case errors.Is(err, ErrHeadResolverUnavailable), err == nil:
		// `err == nil` here is a defence-in-depth fallback
		// — if a caller routes a non-error path into this
		// function we still produce a 502 so the operator
		// has a sensible signal.
		writeJSONError(w, http.StatusBadGateway, "head_resolver_unavailable",
			"head resolver is unavailable; retry shortly")
	default:
		writeJSONError(w, http.StatusBadGateway, "head_resolver_unavailable",
			"head resolver is unavailable; retry shortly")
	}
	h.logger.Warn("mgmtapi."+op+".resolver_failed",
		slog.String("op", op),
		slog.String("repo_url", repoURL),
		slog.String("path", r.URL.Path),
		slog.String("error", errToString(err)),
	)
}

// errToString safely renders an error for structured logging.
// Returns the literal `"<nil>"` for a nil error so a stray
// log line that includes the error attribute regardless of
// nilness still produces a parseable record.
func errToString(err error) string {
	if err == nil {
		return "<nil>"
	}
	return err.Error()
}

// isInvalidUUIDError matches the PostgreSQL SQLSTATE 22P02
// (`invalid_text_representation`) error that the `$1::uuid`
// cast produces when the input is not a valid UUID. Copied
// in spirit from internal/webhookreceiver.isInvalidUUIDError;
// kept local to avoid a cross-package internal dependency.
func isInvalidUUIDError(err error) bool {
	if err == nil {
		return false
	}
	msg := strings.ToLower(err.Error())
	return strings.Contains(msg, "invalid input syntax for type uuid")
}

// isForeignKeyViolation matches PostgreSQL SQLSTATE 23503
// (`foreign_key_violation`). Used to map a delete-during-
// register race onto a 404 instead of a 500. We look at
// the error message text rather than pull in `lib/pq`'s
// typed error struct so the helper works with any
// pq-compatible driver (lib/pq, pgx via stdlib).
func isForeignKeyViolation(err error) bool {
	if err == nil {
		return false
	}
	msg := strings.ToLower(err.Error())
	// pq formats: `pq: insert or update on table "repo_event"
	// violates foreign key constraint ...`. pgx/stdlib:
	// similar shape with the `foreign key constraint`
	// substring.
	return strings.Contains(msg, "foreign key constraint") ||
		strings.Contains(msg, "sqlstate 23503")
}

